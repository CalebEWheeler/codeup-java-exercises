public class StringExercise {
    public static void main(String [] args) {
        String line1 = "We don't need no education";
        String line2 = "We don't need no thought control";
        System.out.println(line1 + "\n" + line2);
        String line3 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(line3);
        String line4 = "In windows, the main drive is usually C:\\ ";
        System.out.println(line4);
        String line5 = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(line5);


    }
}
